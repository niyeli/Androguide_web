<h2><strong>章節導讀</strong></h2>

<blockquote>
  <ol>
  <h3><li><p>第一章 綜觀Andriod生態圈</h3>
  <strong>講解Android行動裝置的生態系統、發展史與利益相關者，討論Android生態系統發展遭遇的挑戰與安全性問題。</strong></p></li>
  <h3><li><p>第二章 Android安全設計與架構</h3>
  <strong>了解安卓系統架構，引入系統安全機制的基礎核心概念(沙盒與權限)，並深入介紹安卓各層次與關鍵安全元件的內部機制。</strong></p></li>
  <h3><li><p>第三章 root Android裝置</h3>
  <strong>介紹獲取Android裝置完全控制權之方法並說明歷史上發生過的一些漏洞攻擊之利用，細節將於第八章中進行介紹。</strong></p></li>
  <h3><li><p>第四章 應用程式安全評估</h3>
  <strong>介紹Android應用相關的安全問題，討論應用開發中常見的安全錯誤，並以案例示範使用通用工具找到問題和技術。</strong></p></li>
  <h3><li><p>第五章 理解Android的攻擊面</h3>
  <strong>說明行動裝置可能遭受攻擊的形式，介紹攻擊的關鍵術語。</strong></p></li>
  <h3><li><p>第六章 使用模糊測試來挖掘漏洞</h3>
  <strong>講述如何使用模糊測試技術來發現Android系統中的軟體漏洞。對Broadcast Receiver、Android上的Chrome、USB攻擊面三個方面進行模糊測試，講述如何發現Android系統中的安全問題。</strong></p></li>
  <h3><li><p>第七章 偵錯與分析安全漏洞</h3>
  <strong>如何分析在Android系統中發現的缺陷和安全漏洞，介紹各種偵錯工具，並設定環境以提高偵錯效率。並以基於WebKit引擎的瀏覽器中一個未修補的安全問題為案例進行深入分析。</strong></p></li>
  <h3><li><p>第八章 使用者空間軟體的漏洞利用</h3>
  <strong>介紹在Android使用者空間軟體中，記憶體破壞漏洞的利用方法，例如堆積的實作、ARM體系架構規範等。最後詳細分析了GingerBreak、zergRush、Mempodroid，以及Android瀏覽器之漏洞利用。</strong></p></li>
  <h3><li><p>第九章 ROP漏洞利用技術</h3>
  <strong>介紹高階利用技術ROP（Return Oriented Programming），以bionic動態連結涵式庫此ROP原始碼為歷來進行介紹。</strong></p></li>
  <h3><li><p>第十章 攻擊內核</h3>
  <strong>對攻擊Android裝置的Linux內核進行介紹，包含如何設定與編譯內核與其模組等。了解從駭客的角度來對內核進行開發和偵錯，最後介紹如何利用數個已公開的內核漏洞。</strong></p></li>
  <h3><li><p>第十一章 攻擊RIL無線介面層</h3>
  <strong>介紹RIL(無線介面層)之工作原理，分析攻擊RIL之方法、介紹SMS續席格式。深入討論如何修改RIL的程式碼，從而對Android系統中處理簡訊的模組進行模糊測試。</strong></p></li>
  <h3><li><p>第十二章 漏洞用緩解技術</h3>
  <strong>介紹Android系統中的安全保護機制是如何被引入Android系統與運作，介紹一些紓解技術，並討論如何進用與對抗這些紓解技術。</strong></p></li>
  <h3><li><p>第十三章 硬體層的攻擊</h3>
  <strong>深入探索通過硬體層面來攻擊Android和其他嵌入式裝置的方法。</strong></p></li>
  <h3><li><p>附錄一 工具</h3>
  <strong>Android開發工具(Android SDK、Android NDK等)、原生工具(BusyBox)、靜態分析工具(Androguard、apktool等)、測試工具、硬體安全工具等之簡單介紹。</strong></p></li>
  <h3><li><p>附錄二 開放原始碼程式碼庫</h3>
  <strong>Google、SoC廠商等。</strong></p></li>
  
  </ol>
</blockquote><br/>


